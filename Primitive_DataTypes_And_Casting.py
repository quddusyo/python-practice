# Integers
# 1 2 3 4 5
x = 5

# Floats
# 2.5, -0.342
fl_x = float(x) #5.0
print(fl_x)

# Strings
# "This is a string"
str_x = str(x) #'5'
print(str_x)

# Booleans
# True // False
print(str_x == x) #False